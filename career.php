  function submitInvestorForm() {
    var firstName = document.getElementById('investor_first_name').value;
    var lastName = document.getElementById('investor_last_name').value;
    var email = document.getElementById('investor_email').value;
    var contact = document.getElementById('investor_contact_number').value;
    var city = document.getElementById('investor_city').value;
    var state = document.getElementById('investor_state').value;
    var pincode = document.getElementById('investor_pincode').value;
    var investor_AcceptTermsNCondtion = document.getElementById('investor_AcceptTermsNCondtion').checked ? 1 : 0;
    var investor_know_about_us = document.getElementById('investor_know_about_us').value;

    if (firstName == '') {
      Swal.fire({
        icon: 'error',
        title: 'First Name is required'
      });
    } else if (email == '') {
      Swal.fire({
        icon: 'error',
        title: 'Email is required'
      });
    } else if (contact == '') {
      Swal.fire({
        icon: 'error',
        title: 'Contact Number is required'
      });
    } else {
      var formData = new FormData();
      formData.append('first_name', firstName);
      formData.append('last_name', lastName);
      formData.append('email', email);
      formData.append('contact_number', contact);
      formData.append('city', city);
      formData.append('state', state);
      formData.append('pincode', pincode);
      formData.append('AcceptTermsNCondtion', investor_AcceptTermsNCondtion);
      formData.append('know_about_us', investor_know_about_us);

      $.ajax({
        url: 'addUsersData.php?action=addInvestorApplication',
        type: 'POST',
        data: formData,
        contentType: false,
        processData: false,
        success: function (response) {
          // Handle response
          var responseJson = JSON.parse(response);
          if (responseJson.success) {
            $('.input-clear-cls').val('');
            $('#investor-applicant-form').addClass('d-none');
            Swal.fire({
              text: "Application has been Successfully Registered",
              icon: "success"
            });
            $('#investor-text-after-apply').removeClass('d-none');
            $('#investor-text-after-apply').text('');
            $('#investor-text-after-apply').append(`<h5 class="bg-sec-theme text-center">Dear ${firstName}</h5>
                        <h5 class="bg-sec-theme text-center">Thank you for your application to Tiffany Finance!</h5>
                        <h5 class="bg-sec-theme text-center">We appreciate your interest and will promptly review your application.</h5>`);
          } else {
            Swal.fire({
              text: responseJson.message,
              icon: "error"
            });
          }
          // You can update the UI or show a message based on the response
        },
        error: function (error) {
          console.error(error);
        }
      });
    }
  }



  function saveJobApplicantData() {
    var firstName = document.getElementById('first_name').value;
    var phone = document.getElementById('phone').value;
    var email = document.getElementById('email').value;
    var resumeFile = document.getElementById('resume_location').files[0];

    if (resumeFile == undefined) {
      Swal.fire({
        icon: 'error',
        title: 'File is required',
        text: 'File size exceeds the allowed limit (' + '300' + ' KB)',
      });
    } else {
      var file_size = resumeFile.size / 1024;
      console.log(file_size);
      if (file_size >= 300) {
        Swal.fire({
          icon: 'error',
          title: 'File Size Error',
